package com.java.oops;

public class Bestie_2 {		// Abstraction using constructor
	String name;
	int number;
	String chocolate;
	String actor;
	String color;
	
	public Bestie_2(String name, int number, String chocolate, String actor,
							String color) {
		super();
		this.name = name;
		this.number = number;
		this.chocolate = chocolate;
		this.actor = actor;
		this.color = color;
	}
	
}
